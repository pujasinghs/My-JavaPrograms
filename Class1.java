package My_Package;

public class Class1 {

	public static void printHypenStar() {
	int star=3,hyphen=2;
	for(int j=1;j<=star;j++) {
		System.out.print("* ");
	}
	for(int k=1;k<=hyphen;k++) {
		System.out.print("- ");
	}
	System.out.println();
	}
	
	public static void main(String[] args) {
		printHypenStar();
		
	}

}
